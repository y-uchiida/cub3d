#include "libft.h"

/*
** 'A' -> 65, 'Z' -> 90
** 'a' -> 97, 'z' -> 122
*/

int	ft_toupper(int c)
{
	if ('a' <= c && c <= 'z')
		return (c - ('a' - 'A'));
	else
		return (c);
}

#include "libft.h"

/*
** 'A' -> 65, 'Z' -> 90
** 'a' -> 97, 'z' -> 122
*/

int	ft_tolower(int c)
{
	if ('A' <= c && c <= 'Z')
		return (c + ('a' - 'A'));
	else
		return (c);
}
